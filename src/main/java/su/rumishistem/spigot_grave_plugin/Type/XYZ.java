package su.rumishistem.spigot_grave_plugin.Type;

public class XYZ {
	private int x;
	private int y;
	private int z;

	public XYZ(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public int get_x() {
		return x;
	}

	public int get_y() {
		return y;
	}

	public int get_z() {
		return z;
	}
}
